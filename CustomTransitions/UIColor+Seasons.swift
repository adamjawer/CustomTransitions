import UIKit

extension UIColor {
    class var autumnColor: UIColor {
        get {
            return UIColor(red: 1, green: 182.0 / 255.0, blue: 38.0 / 255.0, alpha: 1)
        }
    }
    
    class var titleShadowColor: UIColor {
        get {
            return UIColor(red: 30.0 / 255.0, green: 30.0 / 255.0, blue: 30.0 / 255.0, alpha: 1)
        }
    }
}
